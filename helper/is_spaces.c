#include "../minishell.h"

int is_spaces(char *str)
{
    int i;

    i = 0;
    while (str[i])
    {
        if ( (str[i] < 9 || str[i] > 13) && str[i] != 32)
        {
            return (0);
        }
        i++;
    }
    return (1);
}
