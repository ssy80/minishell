#include "minishell.h"

int	ft_isalnum2(int a)
{
	if (a == '_')
		return (8);
	if (a >= 'A' && a <= 'Z')
		return (8);
	if (a >= 'a' && a <= 'z')
		return (8);
	if (a >= '0' && a <= '9')
		return (8);
	return (0);
}
